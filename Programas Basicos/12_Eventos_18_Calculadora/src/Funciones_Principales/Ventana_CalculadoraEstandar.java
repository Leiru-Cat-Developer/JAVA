    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
        if(operacion.equals("null")){
            etiquetaNumeros.setText(cadenaDeNumeros);
        }else if(operacion.equals("SUMAR")){
            numero2 = Double.parseDouble(cadenaDeNumeros);
            resultado = numero1 + numero2;
            etiquetaNumeros.setText(String.format("%.2f", resultado));          //RESULTADO SUMA CON 2 DECIMALES
            cadenaDeNumeros = String.valueOf(resultado);                        //GUARDAMOS EL RESULTADO
            operacion = "nula";
        }else if(operacion.equals("RESTAR")){
            numero2 = Double.parseDouble(cadenaDeNumeros);
            resultado = numero1 - numero2;
            etiquetaNumeros.setText(String.format("%.2f", resultado));          //RESULTADO RESTA CON 2 DECIMALES
            cadenaDeNumeros = String.valueOf(resultado);                        //GUARDAMOS EL RESULTADO
            operacion = "nula";
        }else if(operacion.equals("MULTIPLICAR")){
            numero2 = Double.parseDouble(cadenaDeNumeros);
            resultado = numero1 * numero2;
            etiquetaNumeros.setText(String.format("%.2f", resultado));          //RESULTADO MULTIPLICAR CON 2 DECIMALES
            cadenaDeNumeros = String.valueOf(resultado);                        //GUARDAMOS EL RESULTADO
            operacion = "nula";
        }else if(operacion.equals("DIVIDIR")){
            numero2 = Double.parseDouble(cadenaDeNumeros);
            resultado = numero1 / numero2;
            etiquetaNumeros.setText(String.format("%.2f", resultado));          //RESULTADO DIVIDIR CON 2 DECIMALES
            cadenaDeNumeros = String.valueOf(resultado);                        //GUARDAMOS EL RESULTADO
            operacion = "nula";
        }
        etiquetaMuestra.setText("");
        activacion = true;                                                      //ACTIVACION DEL BOTON
    }//GEN-LAST:event_botonIgualActionPerformed
