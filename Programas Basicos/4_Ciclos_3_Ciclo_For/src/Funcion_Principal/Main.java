package Funcion_Principal;
public class Main {
    public static void main(String[] args) {
        /*
            EL CICLO FOR CONSTA DE TRES ELEMENTOS QUE SE INCLUYEN EN LA
            SIGUIENTE SENTENCIA:
        
            FOT (INICIALIZACION; CONDICION; AUMENTO O DECREMENTO){
            
                PROCESO
        
            }
        */
        for(int Iterador = 10; Iterador >= 1; Iterador--){
            
            System.out.println(Iterador);
            
        }
        /*LA VARIABLE SOLO FUNCIONA DENTRO DE LA MISMA CONDICION, Y CUANDO TERMINE SE INCICIALIZA DE NUEVO*/
    }    
}